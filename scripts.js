// calculation with basic operations
// addition, substraction, multiplication, division

function add(num1, num2) {
    const sum = num1 + num2;
    return sum;
}


function subst(num1, num2) {
    const substraction_result = num1 - num2;
    return substraction_result;
}

function multi(num1, num2) {
    const milti_result = num1 * num2;
    return milti_result;
}

function div(num1, num2) {
    const division_result = num1 / num2;
    return division_result;
}


